<p>Il prossimo avversario dei blucerchiati, che oggi riprenderanno la preparazione,  proprio l&#8217;Atalanta di Ilicic. Prima o poi sarebbe arrivato il momento in cui il trequartista, ex Fiorentina, avrebbe messo piede tra le mura dello stadio Luigi Ferraris. Dall&#8217;altra parte gli avversari troveranno <strong>Gaston Ramirez</strong>, che per l&#8217;Atalanta<strong> fu un vero e proprio incubo</strong> nella stagione 2011/2012. L&#8217;uruguaiano contro gli orobici ha giocato un&#8217;unica partita, 77 minuti per la precisione, che per hanno determinato la debacle sportiva della squadra  allenata all&#8217;epoca da Colantuono. In quei settantasette minuti riusc a procurarsi un rigore, poi realizzato da Di Vaio, segnare un gol e mandare in porta Loria per la terza marcatura del Bologna. Era sempre ottobre, l&#8217;Atalanta si era portata in vantaggio dopo solo 7 minuti ma sul finale del primo tempo e per quasi tutto il secondo &#8211; fino alla sostituzione &#8211; <strong>in campo comand Ramirez</strong>. L&#8217;auspicio  quello che il trequartista sia di nuovo cos decisivo come quando vest la maglia del Bologna, diventando per una giornata la bestia nera della squadra nerazzurra.</p>
